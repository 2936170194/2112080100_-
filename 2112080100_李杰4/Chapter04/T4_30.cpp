 #include <iostream>

using namespace std;

int main ()
{
    double r,d,l,s,pi=3.14159;
    cin>>r;
    d=2*r;
    l=2*pi*r;
    s=pi*r*r;
    cout<<"d:"<<d<<endl;
    cout<<"l:"<<l<<endl;
    cout<<"s:"<<s<<endl;

    return 0;
}
